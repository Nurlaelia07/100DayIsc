package ngoding;

public class Day45 {

    public static void main(String[] args) {
        // Deklarasi array
        String[] nama = new String[5];

        // Mengisi array
        nama[0] = "Laelia";
        nama[1] = "Nike";
        nama[2] = "FItta";
        nama[3] = "Lala";
        nama[4] = "AMalia";

        // Menampilkan array
        for (int i = 0; i < nama.length; i++) {
            System.out.println(nama[i]);
        }
    }
}
